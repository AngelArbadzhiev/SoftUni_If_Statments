
food = str(input())
# banana, apple, kiwi, cherry, lemon и grapes;
if food == "banana" or food == "apple" or food == "kiwi" or food == "cherry" or food == "lemon" or food == "grapes":
    print("fruit")
# tomato, cucumber, pepper и carrot;
if food == "tomato" or food == "cucumber" or food == "pepper" or food == "carrot":
    print("vegetable")
else:
    print("unknown")